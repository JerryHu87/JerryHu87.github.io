#include <bits/stdc++.h>
using namespace std;

int main() {
    int n;
    scanf("%d", &n);
    if(n != 8 && n != 9){
        cout << "answer" << endl;
        return 0;
    }
    int m;
    scanf("%d", &n);
    scanf("%d", &m);
    if(n != m){
        cout << "answer" << endl;
        return 0;
    }
    scanf("%d", &n);
    if(n != 8 && n != 9){
        cout << "answer" << endl;
        return 0;
    }
    cout << "ignore" << endl;
    return 0;
}
